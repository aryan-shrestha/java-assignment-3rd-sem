    private void loginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnMouseClicked
        // TODO add your handling code here:
        String username = usernameText.getText();
        String password = String.valueOf(passwordText.getPassword()) ;
        
        ManagingUser user = new ManagingUser(username, password);
        
       
        if (user.login(user.getUsername(), user.getPassword())){
            this.setVisible(false);
            Home fm = new Home(username);
            fm.setVisible(true);
        } else {
            messageBox.setForeground(new java.awt.Color(255, 0, 51));
            messageBox.setText("Invalid username or password");
        }
        

    }//GEN-LAST:event_loginBtnMouseClicked

